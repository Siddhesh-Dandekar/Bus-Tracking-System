        const busNumberSelect = document.getElementById('bus_number');

        schoolSelect.addEventListener('change', function () {
            const selectedSchoolId = this.value;

            // Check if a school is selected
            if (selectedSchoolId) {
                // Clear any existing options in the bus number dropdown
                busNumberSelect.innerHTML = '<option value="">Select Bus Number</option>';

                // Fetch buses based on the selected school ID using AJAX
                fetch(`../admin/get-buses.php?school_id=${selectedSchoolId}`) // Replace `/get-buses.php` with the actual URL of your PHP script
                    .then(response => response.json())
                    .then(data => {
                        if (data.length > 0) {
                            // Loop through buses and add options to the dropdown
                            for (const bus of data) {
                                const option = document.createElement('option');
                                option.value = bus.bus_number;
                                option.textContent = bus.bus_number;
                                busNumberSelect.appendChild(option);
                            }
                            // Show the bus number dropdown
                            busNumberSelect.classList.remove('hidden');
                        } else {
                            // Handle no buses found for the selected school
                            busNumberSelect.innerHTML = '<option value="">No buses found</option>';
                        }
                    })
                    .catch(error => {
                        console.error('Error fetching buses:', error);
                    });
            } else {
                // Hide the bus number dropdown if no school is selected
                busNumberSelect.classList.add('hidden');
            }
        });
