<?php
session_start();

// Validate user session (assuming 'loggedemail' is the session variable)
if (isset ($_SESSION['loggedemail'])) {
    header("location: location.php");  // Redirect to dashboard if already logged in
    exit();
}

// Check if login form is submitted (using POST method)
if ($_SERVER['REQUEST_METHOD'] === 'POST' && isset ($_POST['login'])) {
    require '../config.php';

    // Sanitize user input (prevent XSS)
    $email = filter_var($_POST["email_id"], FILTER_SANITIZE_EMAIL);
    $password = $_POST["password"]; // Password is not sanitized for comparison

    // Prepare SQL statement (same as login code)
    $sql = "SELECT userid, password, email_id FROM user WHERE email_id = ?";

    $stmt = $conn->prepare($sql);
    $stmt->bind_param("s", $email);

    // ... rest of the login logic remains the same ...
    if ($stmt->execute()) {
        $stmt->bind_result($user_id, $hashed_password, $db_email);
        $stmt->fetch();

        // Verify password using password_verify function (compares hashed password with provided one)
        if (password_verify($password, $hashed_password)) {
            $_SESSION['loggedemail'] = $db_email; // Store user email in session
            $_SESSION["loggeduser"] = true;
            header("location: location.php"); // Redirect to dashboard on successful login
        } else {
            echo "Invalid email or password!";
        }
    } else {
        echo "Error: " . $stmt->error;

        // Consider logging the error for troubleshooting purposes
        error_log("Login error: " . $stmt->error, 3, "/path/to/error.log");
    }

    $stmt->close();
    $conn->close();
}
